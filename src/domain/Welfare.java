package domain;

public class Welfare {
    private String wname;
    private int wsalary;
    private String oldWname;

    public String getWname() {
        return wname;
    }

    public void setWname(String wname) {
        this.wname = wname;
    }

    public int getWsalary() {
        return wsalary;
    }

    public void setWsalary(int wsalary) {
        this.wsalary = wsalary;
    }

    public String getOldWname() {
        return oldWname;
    }

    public void setOldWname(String oldWname) {
        this.oldWname = oldWname;
    }
}
